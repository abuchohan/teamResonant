            <h2>Tomato Spagetti</h2>
            <p>
Prep time: 5 min<br>
Cook time: 20 min<br>
(Serves 2)<br>

Ingredients:<br>
- Half a pack of spagetti<br>
- 4 Cherry tomatoes<br>
- 1/2 Onion<br>
- Tomato sauce<br>
- 7-8 leafs ofBassil<br>
- Salt & Pepper<br>
- Picle vinegret (1 table spoon)<br>
- Olive oil<br>
            </p>
            </div>
        </div>
        <div class="page-article1-text">
        <p>Step 1<br>
Spread some Olive oil on a pan and turn the heat on medium. Pour some water in a pot, add Salt to taste and leave untill it starts to buble. <br>
Step 2<br>
While the stoves are heating up, dice the onions and after that the tomatoes. Add the onions to the pan and leave untill the edges become brown. Halfway through add the chopped tomatoes in. Meanwhile if the pot has started to buble add the spagettiin and lower the heat enough to not let it pour out. Don’t forget to stirr ! Same counts for the onions.<br>
Step 3<br>
When the onions are ready, add the Tomato sauce to the pan. Add some Salt and Pepper to taste and add the bassil by riping the leafs in half. Leave some leafs for the end. Add the spoon of vinegret and stirr it through. <br>
Step 4 <br>
Let the sauce cook for 5-10 minutes and when the spagetti is ready feel free to drain the water.
Enjoy!</p>
