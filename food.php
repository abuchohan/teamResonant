            <h2>Keema</h2>
            <p>
Prep time: 5 Mins<br>
Cook time: 25 Minutes<br>
(Serves 4, but does it really?)<br>
Really easy to make Indian dish. You can make it with one pot a knife and a cutting board. Goes really well with pitta bread.<br>

Ingredients:<br>
-500g Mince Meat<br>
-2 fresh tomatoes<br>
-2 Medium Onions<br>
-1 Tbsp. Chilli Powder (Can add more to taste)<br>
-1 Tbsp. Turmeric Powder <br>
-1 Frozen Cube of Ginger and Garlic<br>
-1 Tbsp. Salt and Pepper<br>
-1 Tbsp. Cumin Powder<br>
            </p>
            </div>
        </div>
        <div class="page-article1-text">
        <p>Step 1. (Prep)<br>
Firstly finely dice your onion. Chop up the tomatoes. Get a small bowl and add all the following spices into the bowl.<br>
-1 Tbsp. Chilli Powder (Can add more to taste)<br>
-1 Tbsp. Turmeric Powder <br>
-1 Frozen Cube of Ginger and Garlic<br>
-1 Tbsp. Salt and Pepper<br>
-1 Tbsp. Cumin Powder<br>
Step 2.<br>
Get a pot with and put a little squirt of oil and turn the heat up. Add the diced onions to the pot and brown.<br>
Step 3.<br>
Add the chopped tomatoes once the onions are brown. Add them to the pot along with the meat. And the spices. Mix well.
Lower the heat and Leave to cook for 20 minutes.<br>
Step 4.<br>
After 20 minutes turn back up to full heat until it isn’t too runny(This might take a you a couple of times after making it to get a consistency you like).<br>

After this enjoy! Serving suggestion with Pitta bread.
 that's how i done it
 quite light tone</p>
