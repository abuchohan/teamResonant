            <h2>Curry and Rice</h2>
            <p>(Time: 15min) (premade sauce)<br>
Simple and consistent dish every time. Quick and easy.<br>
Ingredients:<br>
-400g Chicken<br>
-2 Cups rice<br>
2 tbsp. Oil<br>
2 tbsp. salt and pepper<br>
Optional:<br>
Cinnamon stick<br>
            </p>
            </div>
        </div>
             <div class="page-article1-text">
        <p>Step 1.<br>
Pre heat oven to 200 degrees (Fan oven) Leave for 20 minutes to pre heat oven, this way your chicken will cook evenly. Wash, clean and cut the chicken in strips or cubes. Put on a baking tray and a drizzle of oil and leave for 14 minutes.<br>
Step 2.<br>
Clean rice until the water is clear. Drain the water and add 4 cups of water (2 cups for 1 cup of rice). Put in oil salt and pepper. (Add cinnamon to rice to give it fragrance). Bring to boil and then turn heat to low and leave until water is gone.<br>
Step 3.<br>
After chicken is done heat up frying pan and pour the curry sauce in there. Add chicken then reduce heat and simmer for 5-10 minutes.<br>
Step 4.<br>
Pour the rice into plate and add the desired amount of chicken with sauce. Then dig in! You can enjoy this with garlic bread.</p>
